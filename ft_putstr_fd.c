#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
/* writes s to fd */
{
	int		i;
	char	c;

	if (!s)
		s = "\0";
	if (!fd)
		fd = 1;
	i = 0;
	while (s[i] != '\0')
	{
		c = s[i];
		write(fd, &c, 1);
		i++;
	}
}
